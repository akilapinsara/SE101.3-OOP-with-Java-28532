public abstract class Container {
    private double hei;
    private double rad;

    public abstract double calVolume();

    public double getHei() {
        return hei;
    }

    public void setHei(double hei) {
        this.hei = hei;
    }

    public double getRad() {
        return rad;
    }

    public void setRad(double rad) {
        this.rad = rad;
    }
}



